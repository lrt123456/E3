//根据数据表定义一个Java bean
public class Data1 {
	int id;
	String v;
	String weight;
	String na;
	String ratio;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

	public String getV() {
		return v;
	}
	public void setV(String v) {
		this.v = v;
	}

	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}

	public String getNa() {
		return na;
	}
	public void setNa(String na) {
		this.na = na;
	}

	public String getRatio() {
		return ratio;
	}
	public void setRatio(String ratio) {
		this.ratio = ratio;
	}



}
